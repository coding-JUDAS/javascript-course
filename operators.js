// Operators
// Increment and Decrement Operators

var number = 0;
// Postfix
console.log("psotfix");
console.log(number++);
console.log(number);
console.log(number--);
console.log(number);
console.log();

//Prefix
var number2 = 5;
console.log("prefix");
console.log(++number2);
console.log(number2);
console.log(--number2);
console.log(number2);
console.log();

// Comparison Operator
console.log("for numbers: [ ==, !=, <, <=, >, >= ]")
console.log(10 == 10);
console.log(10 != 10);
console.log(10 < 10);
console.log(10 <= 10);
console.log(10 > 10);
console.log(10 >= 10);
console.log()

console.log("for strings and chars: [ ==>, <!= ]");
console.log("A" == "B");
console.log("A" != "B");
console.log();

// Logic Operators
console.log("for LOGIC operators: [ &&, ||]");
console.log(10 == 10 || "A" == "B");
console.log(10 == 10 && "A" == "B");
console.log("A" != "B");
console.log();